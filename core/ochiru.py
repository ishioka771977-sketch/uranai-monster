def _planet_sign(bundle: DivinationBundle, jp_name: str) -> str:
    w = bundle.western
    for p in (w.planets or []):
        if p.name == jp_name:
            return p.sign or ""
    return ""


def _kaki_palace_name(bundle: DivinationBundle) -> str:
    """紫微斗数: 化忌が付いている宮の名前を返す（なければ ""）"""
    z = bundle.ziwei
    if z is None:
        return ""
    for pal in (z.palaces or []):
        for h in (pal.sihua or []):
            if "忌" in h:
                return pal.palace_name or ""
    return ""


def _gogyo_lines(bundle: DivinationBundle) -> list[str]:
    """命式の五行偏りから「堕ちやすい状態」体感文を作る"""
    g = bundle.sanmei.gogyo_balance or {}
    total = sum(v for v in g.values() if isinstance(v, (int, float)))
    out: list[str] = []
    if total <= 0:
        return out
    for el in ("木", "火", "土", "金", "水"):
        pct = g.get(el, 0) / total * 100
        if pct >= 40 and el in _GOGYO_OVER:
            out.append(_GOGYO_OVER[el])
    if g.get("水", 0) / total * 100 <= 10:
        out.append(_GOGYO_LACK["水"])
    if g.get("火", 0) / total * 100 <= 10:
        out.append(_GOGYO_LACK["火"])
    return out


