def generate_ziwei_reading(bundle: DivinationBundle) -> dict:
    """紫微斗数コースの鑑定文を生成"""
    z = bundle.ziwei
    if z is None:
        return _ziwei_fallback(bundle)

    # 命盤情報をテキスト化
    palaces_text = ""
    for p in z.palaces:
        stars = '・'.join(p.main_stars) if p.main_stars else '空宮'
        aux = '、'.join(p.aux_stars[:4]) if p.aux_stars else 'なし'
        sihua = '、'.join(p.sihua) if p.sihua else ''
        palaces_text += f"  {p.palace_name}（{p.stem}{p.branch}）: 主星={stars} / 副星={aux}"
        if sihua:
            palaces_text += f" / 四化={sihua}"
        palaces_text += "\n"

    sihua_text = '、'.join(f'{star}({hua})' for star, hua in z.sihua_assignments.items())

    # 大限のうち現在の年齢に近い部分
    current_age = date.today().year - bundle.person.birth_date.year
    current_daxian = ""
    for i, (s, e) in enumerate(z.da_xian_ages):
        if s <= current_age <= e:
            palace = z.palaces[i] if i < len(z.palaces) else None
            pname = palace.palace_name if palace else '不明'
            current_daxian = f"第{i+1}限 {s}〜{e}歳（{pname}）"
            break

    # 流年命宮の計算（v3 動的データ）
    # 紫微斗数では流年命宮 = 流年地支に対応する宮
    # 例: 2026年は丙午年なので、流年命宮は午宮
    from core.ziwei import BRANCHES as _ZW_BRANCHES
    current_year = date.today().year
    flow_branch_idx = (current_year - 4) % 12
    flow_branch = _ZW_BRANCHES[flow_branch_idx]
    flow_palace = next((p for p in z.palaces if p.branch == flow_branch), None)
    flow_palace_name = flow_palace.palace_name if flow_palace else "不明"
    flow_year_text = f"流年命宮: {flow_palace_name}（{flow_branch}宮）"

    # 身宮との重なりチェック（=「裏の顔が表に出る年」）
    flow_hits_shen = (flow_branch == z.shen_gong_branch)
    flow_hits_ming = (flow_branch == z.ming_gong_branch)
    if flow_hits_shen:
        flow_year_text += f"  ★★★ {current_year}年は流年命宮が身宮に重なる年 = 「裏の顔が表に出る年」"
    elif flow_hits_ming:
        flow_year_text += f"  ★ {current_year}年は流年命宮が命宮に重なる年 = 「素の自分が試される年」"

